void get_time(void){
RTC_DateTypeDef sDate;
RTC_TimeTypeDef sTime;

	// Get the RTC current Time
	HAL_RTC_GetTime(&hrtc, &sTime, RTC_FORMAT_BIN);
	// Get the RTC current Date
	HAL_RTC_GetDate(&hrtc, &sDate, RTC_FORMAT_BIN);
	// Display time Format: hh: mm: ss
	sprintf( (char *)showTime, "%02d:%02d:%02d\n", sTime.Hours, sTime.Minutes, sTime.Seconds);
	// Display date Format: dd-mm-yy
	sprintf( (char *)showDate, "%04d-%02d-%2d\t", 2000 + sDate.Year, sDate.Month, sDate.Date);

}

void set_time(uint8_t hh, uint8_t mm, uint8_t ss){
RTC_TimeTypeDef sTime;

	sTime. Hours = hh; // set hours
	sTime.Minutes = mm; // set minutes
	sTime. Seconds = ss; // set seconds

	HAL_RTC_SetTime(&hrtc, &sTime, RTC_FORMAT_BIN) ;
}

void set_date(uint8_t ww, uint8_t mm, uint8_t dd, uint8_t yy){
// WeekDay: MONDAY(1) ~ SUNDAY (7)     Month: JANUARY(1) ~ DECEMBER(12)|
RTC_DateTypeDef sDate;
	sDate.WeekDay = ww; // day RTC_WEEKDAY_THURSDAY
	sDate.Month = mm; // month RTC_MONTH_FEBRUARY
	sDate.Date = dd; // date
	sDate.Year = yy; // yea

	HAL_RTC_SetDate(&hrtc, &sDate, RTC_FORMAT_BIN);

}

